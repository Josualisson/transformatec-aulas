let contador = 0;

//executa o bloco de código antes de verificar a condição de parada
do { // do = faça
    
    contador++;
    console.log(contador);

//verificação da condição de parada após a execução do código
} while (contador < 10); // while = enquanto

//leitura do código = faça contar 1 por 1 até 10 enquanto o contador for menor que 10 = então le vai contar até 10
